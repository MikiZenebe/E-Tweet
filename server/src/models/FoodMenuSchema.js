import mongoose, { model, Schema } from "mongoose";

const FoodMenuSchema = new Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
    min: 0,
  },
  imageUrl: {
    type: String,
    required: true,
  },
});

const Food = model("Food", FoodMenuSchema);

export default Food;
